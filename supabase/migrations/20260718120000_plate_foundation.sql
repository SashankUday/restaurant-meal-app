create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key,
  email text not null unique,
  created_at timestamptz not null default now(),
  constraint profiles_email_format check (email = lower(email) and position('@' in email) > 1)
);

create table if not exists public.restaurants (
  id bigint generated by default as identity primary key,
  name text not null unique,
  area text not null,
  cuisine text not null,
  latitude double precision not null check (latitude between -90 and 90),
  longitude double precision not null check (longitude between -180 and 180),
  description text not null default '',
  created_at timestamptz not null default now()
);

create table if not exists public.dishes (
  id bigint generated by default as identity primary key,
  restaurant_id bigint not null references public.restaurants(id) on delete cascade,
  name text not null,
  price numeric(7, 2) not null check (price >= 0),
  description text not null default '',
  course text not null default 'mains' check (course in ('starters', 'mains', 'desserts', 'drinks')),
  menu_position integer not null default 0 check (menu_position >= 0),
  diets text[] not null default '{}',
  allergens text[] not null default '{}',
  crowd_tags jsonb not null default '{}',
  sponsored boolean not null default false,
  created_at timestamptz not null default now(),
  unique (restaurant_id, name)
);

